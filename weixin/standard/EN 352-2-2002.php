<p><strong>EN352-2-2002</strong><br />
  按照标准耳塞必须符合EN13819-1:2002的公称直径。 <br />
  耳塞制作材料不得使用任何已知可能引起皮肤红肿，皮肤疾病，过敏反应，也没有任何其他的对健康的不良影响的材料。 <br />
  耳塞材料当接触汗，耳垢或其他可能在耳道内存在的物质时，材料不会产生化学变化或产生其他化学反应。 <br />
  耳塞应按照标准测试时不开裂，也不得任何部分耳塞脱落。 <br />
  最小衰减要求 </p>
