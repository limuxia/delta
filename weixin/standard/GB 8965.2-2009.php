<p><strong>GB8965.2-2009</strong><strong>防护服装</strong><strong> </strong><strong>阻燃防护（第</strong><strong>2</strong><strong>部分焊接服）</strong><strong> </strong><br />
  焊接服应分级使用:<br />
  A级应用环境：操作人员头部及躯干局部或整体暴露于焊接及相关作业过程产生的由上而下坠落的熔滴飞溅的环境之中,或操作人员处于操作位置或空间受限制无法有效躲避熔滴飞溅和弧光辐射。 <br />
  B级应用环境：操作人员身体局部暴露于焊接及相关作业过程中产生熔滴飞溅和弧光辐射的环境中。 <br />
  C级应用环境：焊接或切割过程中没有或有很少火焰或弧光辐射，金属熔滴飞溅很少。 <br />
  面料性能： <br />
  抗熔融金属冲击性能（洗涤30次后）：经15滴金属熔滴冲击后，试样温升不超过40K。 <br />
  热防护系数TPP/( kW﹒s/m2)：皮肤直接接触，≥126；皮肤与服装有间隙，≥250。 <br />
  甲醛含量：≤75 mg/kg<br />
  pH值：4.0~9.0<br />
  缩水率小于2.5%<br />
  缝纫线在260℃下持续5min，不出现熔融和烧焦的现象 <br />
  缝纫线的单向强力不小于10N。 <br />
