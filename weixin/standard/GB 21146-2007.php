<p><strong>GB21146-2007</strong><strong>个体防护装备职业鞋规范：</strong><strong> </strong><br />
  定义了安全鞋与防护鞋的区别，将具有防护包头的专业鞋定义为安全鞋，将不具有保护包头的鞋款成为职业鞋。 <br />
  其中将防刺穿鞋、防静电鞋、导电鞋等都纳入职业鞋范围（无保护包头）。并分成低帮鞋、高腰鞋、半筒靴、高筒靴、长靴5种款式。 <br />
  技术要求如下： </p>
<table border="1" cellspacing="0" cellpadding="0" width="100%">
  <tr>
    <td width="22%"><br />
      鞋帮最小厚度 </td>
    <td width="77%"><p>橡胶≥1.5mm，聚合材料≥1.0mm</p></td>
  </tr>
  <tr>
    <td width="22%"><p>撕裂强度 </p></td>
    <td width="77%"><p>皮革≥120N，织物或纺织品≥60N</p></td>
  </tr>
  <tr>
    <td width="22%"><p>抗张强度 </p></td>
    <td width="77%"><p>皮革类≥15N/mm2</p></td>
  </tr>
  <tr>
    <td width="22%"><p>扯断强力 </p></td>
    <td width="77%"><p>橡胶类≥180N</p></td>
  </tr>
  <tr>
    <td width="22%"><p>定伸应力 </p></td>
    <td width="77%"><p>聚合材料类：1.3~4.6 N/mm2，扯断伸长率≥250%</p></td>
  </tr>
  <tr>
    <td width="22%"><p>渗透性 </p></td>
    <td width="77%"><p>水蒸气渗透率≥0.8mg/(cm2﹒h)；水蒸气系数≥15 mg/cm2</p></td>
  </tr>
  <tr>
    <td width="22%"><p>PH值 </p></td>
    <td width="77%"><p>皮革类PH值≥3.2</p></td>
  </tr>
  <tr>
    <td width="22%"><p>耐磨性 </p></td>
    <td width="77%"><p>干式测试25600转；湿式测试12800转 </p></td>
  </tr>
</table>
