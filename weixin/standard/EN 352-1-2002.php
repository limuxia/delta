<p><strong>EN352-1-2002</strong><br />
  耳罩应分为三个尺寸：中等尺寸、小尺寸和大尺寸。 <br />
  耳罩可能与皮肤接触的那些部分应当是不染色，柔软和柔韧的材料，而不是已知的可能引起皮肤刺激，过敏反应或与任何其他对健康的不利影响的材料。 <br />
  所有的耳罩部分应呈圆形，表面光滑无锋利的边缘。 <br />
  所有通用的耳罩质量超过150克的，应提供头带。 <br />
  根据相关标准测试时，耳罩的夹具，耳垫和板之间应该是紧密连接的，提供有效的声音阻隔。 <br />
  按照标准测试时，每个试样的头带给予头部的压力力不得大于14 N.并且通过部件可以调节这14N一下的力的强度。 <br />
  按照标准测试时，每个试样的气垫压力应不大于4500 Pa。 <br />
