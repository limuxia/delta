<p>防水性能：在静态水压值为1961Pa时，3h不透水。靴帮皮革沾水性能不低于3级 <br />
  鞋底隔热性能：将鞋置于（150±4）℃的电热板上，在鞋的四周装砂直到外底上边缘为止，鞋内装直径5mm的钢珠4kg。经20分钟时间，记录内底表面试验后的温度。两温度之差应不大于22℃。 <br />
  鞋质量：每双鞋不应大于1.5kg </p>
