<p><strong>GB21148-2007</strong><strong>个体防护装备安全鞋规范：</strong> <br />
  定义了鞋的前部能承受200J能量的冲击和15KN的压力的鞋才能称为安全鞋。并分成低帮鞋、高腰鞋、半筒靴、高筒靴、长靴5种款式。 <br />
  主要性能要求如下表： </p>
<table border="1" cellspacing="0" cellpadding="0" width="100%">
  <tr>
    <td width="18%"><br />
      抗冲击性 </td>
    <td width="81%"><p>承受至少（200±4）J冲击能量冲击后，保护包头内最小间距12.5~15.0</p></td>
  </tr>
  <tr>
    <td width="18%"><p>耐眼里性 </p></td>
    <td width="81%"><p>在（15±1）kN压力下，保护包头内最小间距12.5~15.0</p></td>
  </tr>
  <tr>
    <td width="18%"><p>金属包头 </p></td>
    <td width="81%"><p>按规定测试后，腐蚀区域不超过5处，每处面积不应超过2.5mm</p></td>
  </tr>
  <tr>
    <td width="18%"><p>泄漏性 </p></td>
    <td width="81%"><p>成鞋按规定测试，应没有空气泄漏 </p></td>
  </tr>
  <tr>
    <td width="18%"><p>鞋帮厚度 </p></td>
    <td width="81%"><p>全橡胶鞋帮任何一处厚度≥1.5mm，全聚合材料鞋帮任何一处厚度≥1.0mm</p></td>
  </tr>
  <tr>
    <td width="18%"><p>鞋帮耐折性 </p></td>
    <td width="81%"><p>全橡胶鞋连续屈挠125000次；全聚合材料鞋连续屈挠150000次，无裂痕 </p></td>
  </tr>
  <tr>
    <td width="18%"><p>鞋外底耐折性 </p></td>
    <td width="81%"><p>连续屈挠30000次，切口增长不应大于4mm</p></td>
  </tr>
  <tr>
    <td width="18%"><p>抗刺穿 </p></td>
    <td width="81%"><p>穿透鞋底所需的力不小1100kN（防刺穿鞋特有） </p></td>
  </tr>
  <tr>
    <td width="18%"><p>防静电 </p></td>
    <td width="81%"><p>电阻值大于105小于109欧姆（防静电鞋特有） </p></td>
  </tr>
</table>
