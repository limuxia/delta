<p><strong>GB/T23463-2009</strong><strong>防护服</strong><strong> </strong><strong>微波辐射防护服</strong><strong> </strong><br />
  微波辐射防护服的防护性能，以穿着防护服后，人体头部、胸部、下腹部的电场强度低于GJB5313规定的对应频率下的暴露限值为基本要求。 <br />
  不允许使用孤立与外露的金属件，微波辐射防护服应采用密封独立包装并加入抗氧剂，以防金属化纺织品的金属层氧化。 <br />
  按屏蔽效能标称值的高低分为3个等级，用户可以更具屏蔽效能SE的定义，在实测工作场所电场强度为E0（V/m）的环境中选择适当的防护等级 </p>
