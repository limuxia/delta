<p><strong>GB/T16556-2007</strong><strong>自给开路式压缩空气呼吸器标准规范：</strong><strong> </strong><br />
  工业空气呼吸器，标记代号为G;消防和应急空气呼吸器标记代号为X.<br />
  在0.1MPa、20℃下额定储气量Q分类如下： </p>
<table border="1" cellspacing="0" cellpadding="0">
  <tr>
    <td><br />
      标记号 </td>
    <td><p align="center">6</p></td>
    <td><p align="center">8</p></td>
    <td><p align="center">12</p></td>
    <td><p align="center">16</p></td>
    <td><p align="center">20</p></td>
    <td><p align="center">≥24</p></td>
  </tr>
  <tr>
    <td><p align="center">Q(L)</p></td>
    <td><p align="center">600≤Q＜800</p></td>
    <td><p align="center">800≤Q＜1200</p></td>
    <td><p align="center">1200≤Q＜1600</p></td>
    <td><p align="center">1600≤Q＜2000</p></td>
    <td><p align="center">2000≤Q＜2400</p></td>
    <td><p align="center">Q≥2400</p></td>
  </tr>
</table>
<p>技术标准： </p>
<table border="1" cellspacing="0" cellpadding="0" width="100%">
  <tr>
    <td width="19%"><br />
      工作温度 </td>
    <td width="80%"><p>-30℃~60℃，温度区间内呼吸阻力应不大于1kPa</p></td>
  </tr>
  <tr>
    <td width="19%"><p>阻燃 </p></td>
    <td width="80%"><p>背带和代扣材料应具有阻燃性，续燃时间不超过5s，平均损毁长度≤100mm</p></td>
  </tr>
  <tr>
    <td width="19%"><p>高压部件 </p></td>
    <td width="80%"><p>金属高压管、阀、连接部件应能承受1.5倍气瓶额定工作压力 </p></td>
  </tr>
  <tr>
    <td width="19%"><p>气瓶 </p></td>
    <td width="80%"><p>钢制气瓶符合GB5099标准，符合气瓶符合DOT-CFFC标准 </p></td>
  </tr>
  <tr>
    <td width="19%"><p>警报器 </p></td>
    <td width="80%"><p>当气瓶内压力下降至（5.5±0.5）MPa,警报器启动，声强不小于90dB（A） </p></td>
  </tr>
  <tr>
    <td width="19%"><p>呼吸阻力 </p></td>
    <td width="80%"><p>面罩呼吸腔内保持不超过500Pa正压 </p></td>
  </tr>
  <tr>
    <td width="19%"><p>静态压力 </p></td>
    <td width="80%"><p>平衡条件下，面罩呼吸腔内保持不超过500Pa正压 </p></td>
  </tr>
  <tr>
    <td width="19%"><p>减压阀 </p></td>
    <td width="80%"><p>在输入不差过3MPa的压力下，减压器泄压阀应能通过400L/min的气流 </p></td>
  </tr>
</table>
