<p>织物酸碱类化学品防护服洗前和洗后拒液效率不应小于90%<br />
  织物酸碱类化学品防护服对硫酸、盐酸、硫酸、硝酸、氢氧化钠强力下降率≤30%，非织物酸碱类化学品防护服对硫酸、盐酸、氢氧化钠、硝酸强力下降率≤50%。 <br />
  织物酸碱类化学品防护服的接缝断裂强力不应小于98N，非织物酸碱类化学品防护服的接缝断裂强力不应小于45N。 <br />
  非织物酸碱类化学品防护服经过100圈磨损后应无破洞。 <br />
  非织物酸碱类化学品防护服屈挠1000次后应无破损或断裂。 <br />
  非织物酸碱类化学品防护服穿透力应大于10N。 <br />
  织物酸碱类化学品防护服甲醛含量应小于或等于75mg/kg。 <br />
  织物酸碱类化学品防护服pH值应在4.0~7.5之间。 </p>
