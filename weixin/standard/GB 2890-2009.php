<p>防护气体的容量。防毒过滤元件有1~4个级别的容量规格： <br />
  1级表示一般能力的防护时间；2级表示中等能力的防护时间；3级表示高等能力的防护时间；4级表示特等能力的防护时间。 <br />
  相同浓度下，高级别的过滤元件防毒时间通常更长。 <br />
  P1的效率是95%，P2为99%，P3为99%，要求同时适合防油性和非油性的颗粒物。全面罩的泄漏率小于0.05%，半面罩的泄漏率小于2%。 </p>
