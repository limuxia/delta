<p>外壳材料拉伸强度 <br />
  外壳材料应能承受最小拉力为450 N，对于超过50％的伸长率的材料，这一要求是不适用的。 <br />
  耐撕裂外壳材料 <br />
  外壳材料的撕裂力应在不低于25 N。 <br />
  外壳接缝强度 <br />
  外壳材料的接缝强度应至少225 N，对于超过50％的伸长率的材料，这一要求是不适用的。 </p>
