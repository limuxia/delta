<p><strong>GB/T18664-2002</strong><strong>呼吸防护用品的选择、使用与维护</strong><strong> </strong><br />
  识别有害环境性质，应识别作业中的有害环境，了解以下情况:<br />
  a)是否能够识别有害环境;<br />
  b)是否缺氧及氧气浓度值;<br />
  c)是否存在空气污染物及其浓度;<br />
  d)空气污染物存在形态，是颗粒物、气体或蒸汽，还是它们的组合； <br />
  按照下述方法判定危害程度:<br />
  a) 如果有害环境性质未知，应作为 IDLH环境;<br />
  b) 如果缺氧，或无法确定是否缺氧，应作为IDLH环境;<br />
  c) 如果空气污染物浓度未知、达到或超过 IDLH浓度，应作为IDLH环境;<br />
  d) 若空气污染物浓度未超过 IDLH浓度，应根据国家有关的职业卫生标准规定浓度按式(1)确定危害因数 ;若同时存在一种以上的空气污染物，应分别计算每种空气污染物的危害因数，取数值最大的作为危害因数。 <br />
  危害因数=空气污染物浓度/国家职业卫生标准规定浓度 <br />
