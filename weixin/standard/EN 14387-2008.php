<p><strong>EN14387-2008</strong><br />
  气体滤罐和化合物滤罐 <br />
  主要分为如下类别： <br />
  A型 针对沸点&gt; 65℃的某些有机气体及蒸气。 <br />
  B型 针对某些无机气体和蒸气。 <br />
  E型 针对二氧化硫等酸性气体和蒸气。 <br />
  K型 针对氨及有机氨衍生物。 <br />
  AX型 单次使用，针对某些沸点≤65°C的有机气体及蒸气。 <br />
  SX型 单次使用，针对特定命名制造商指定的气体和蒸气。 <br />
  气体过滤器的类型A，B，E和K在容量方面归类如下： <br />
  1级 低容量过滤器;<br />
  2级 中等容量的过滤器;<br />
  3级 高容量过滤器。 <br />
