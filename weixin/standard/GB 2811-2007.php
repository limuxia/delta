<p><strong>GB2811-2007</strong><strong>安全帽国家标准规范：</strong><strong> </strong><br />
  主要规定了安全帽的相关尺寸，帽壳内部尺寸，长195~250mm，宽170~220mm，高120~150mm；帽舌为10~70mm；帽沿≤70mm；佩戴高度应为80~90mm；垂直间距≤50mm；水平间距为5~20mm。 <br />
  其中技术部分主要技术指标如下： </p>
<div align="center">
  <table border="1" cellspacing="0" cellpadding="0" width="100%">
    <tr>
      <td width="21%"><br />
        冲击吸收性能 </td>
      <td width="78%"><p>冲击测试传到头模上力不超过4900N,帽壳不得有碎片脱落 </p></td>
    </tr>
    <tr>
      <td width="21%"><p>抗穿刺性能 </p></td>
      <td width="78%"><p>按照测试钢锥不得接触头模表面,帽壳不得有碎片脱落 </p></td>
    </tr>
    <tr>
      <td width="21%"><p>下颚带强度 </p></td>
      <td width="78%"><p>破断力150N~250N之间 </p></td>
    </tr>
    <tr>
      <td width="21%"><p>环境温度 </p></td>
      <td width="78%"><p>高温+50℃±2℃      低温-10℃±2℃ </p></td>
    </tr>
    <tr>
      <td width="21%"><p>防静电性 </p></td>
