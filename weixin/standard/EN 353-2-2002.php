<p><strong>EN353-2-2002</strong><br />
  定向型坠落防止装置 <br />
  必须符合EN363:1992指定的设计和人体工程学的一般要。 <br />
  柔性的锚固线应是一种人工合成的纤维绳或钢丝绳。锚固线的材料应符合相关标准要求。 <br />
  柔性的锚线应在固定点上固定，并应配备锁止的移动锚固点。 <br />
  当使用5公斤的制导型重锤测试时，不管在何种情况下发生坠落都必须立即锁止，并保持锁定状态，直到人为释放。 <br />
  合成纤维类锚固线应维持在至少22 kN的力和钢丝绳力锚固线须维持至少为15千牛的力。 <br />
  当使用100kg质量具有刚性钢材作为测试时，制动力Fmax不得超过6.0 kN和锁止距离不得超过3.0米。 <br />
  当使用150kg质量具有刚性钢材作为测试时，不得脱落。 </p>
