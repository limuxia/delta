<p><strong>EN795-2012</strong><br />
  防坠落保护锚固装置要求： <br />
  A类 <br />
  A1类包括设计成固定到垂直，水平和倾斜面的结构锚。例如墙壁，立柱，门楣上 <br />
  <img src="pictures/standard.picture/EN795-2012.picture.1.JPG" width="100%" /> <br />
  1：结构锚  2：锚点 <br />
  A2类包括斜屋顶结构锚固设计 <br />
  <img src="pictures/standard.picture/EN795-2012.picture.2.JPG" width="100%" /> <br />
  1：结构锚  2：锚点 <br />
  B类 <br />
  B类包括可移动式临时锚固装置 <br />
  <img src="pictures/standard.picture/EN795-2012.picture.3.JPG" width="100%" /> <br />
  2：锚点 <br />
  C类 <br />
  C类包括锚固设备整体，在工作者水平方向的柔性连接。应理解为是一条从水平方向偏离不超过15°的水平线。 <br />
  <img src="pictures/standard.picture/EN795-2012.picture.4.JPG" width="100%" /> <br />
  1：两端结构锚  2：中间点 3：移动式锚固点 4：锚固线 <br />
  D类 <br />
  D类包括锚固装置，采用水平刚性固定轨道 <br />
  <img src="pictures/standard.picture/EN795-2012.picture.5.JPG" width="100%" /> <br />
  1：锚固钢轨 2：移动式锚固点 <br />
  E类 <br />
  E类包括水平表面上使用的自重锚。适用从水平方向偏离不超过5°的水平表面。 <br />
  <img src="pictures/standard.picture/EN795-2012.picture.6.JPG" width="100%" /> <br />
  1：结构锚 <br />
  不同的锚固方式都应进行静态测试，按照标准不同的测试方式在锚固方向上施加至少10kN的力，保持3分钟。 </p>
