<p><strong>EN 407-2004</strong><br />
  该标准说明防热和耐火手套的性能标准等级和标识，在以下一种或几种因素的作用下，可以保护手不受伤害：热接触，火焰，热传导，辐射热，小点熔化金属的溅射或大点熔化金属的溅射。试验得出性能的标准而不是防护的标准。 <br />
  机械要求:手套在耐磨损和抗撕裂上必须符合EN388的标准中的1级以上。 <br />
