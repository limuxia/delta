<p><strong>EN340-2003</strong><br />
  防护服不能对使用者的卫生和健康状况造成不利影响。 包括：含铬量 VI / 金属材料中镍的排放量/ pH 值/ 色牢度 / 偶氮染料含量（致癌物质含量） <br />
  防护服的舒适度应当与其防护性能，外部环境，工作人员活动强度和防护服使用寿命一致。 <br />
  防护服尺寸要求&nbsp;:</p>
