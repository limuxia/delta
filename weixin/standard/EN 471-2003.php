<p>逆反射材料条应不小于50mm宽,但对于缚带,逆反射材料条应不小于30mm宽。 <br />
  环绕连体罩衣躯干部位的逆反射材料应是不小于50mm宽的水平的两条。 <br />
  茄克、马甲、背心在躯干部位须有间距不小于50mm的水平的两条逆反射材料。两肩应各有一条连接前后两片的逆反射材料,躯干部位的逆反射条中下面的一条距服装底襟的距离应不小于50mm。 <br />
  夹克、马甲和背心应有一条逆反射材料绕着躯干，两肩应各有一条逆反射材料从前到后与躯干的逆反射材料相联,躯干部位的逆反射条应不低于夹克、背心、马甲的底襟的上方50mm处。 <br />
  夹克、马甲、背心有两条水平逆反射材料最少相距50mm绕着躯干，躯干部位的逆反射条中下面的一条距服装底襟的距离应不小于50mm。 <br />
  连身工作服、吊带裤和腰带裤应有两条相距不小于50mm的逆反射材料。水平绕着每条腿,上面的一条带子的上方应不高于裤腿底部的350mm,下面一条带子的底边应不低于裤腿底边上方50mm。 </p>
