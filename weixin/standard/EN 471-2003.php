<p><strong>EN471-2003</strong><br />
  该标准根据警示服所用材料的最小面积分为三个等级,对三个级别的警示服分别做了说明。3级服装比2级服装有更高的醒目程度,而2级服装又大大优于1级服装。技术要求如下表： </p>
